int	ft_strcmp(char *s1, char *s2)
{
	int	j;
	int	s;
	int	ss;

	s = 0;
	while (s1[s] != '\0')
		s++;
	ss = 0;
	while (s2[ss] != '\0')
		ss++;
	j = 0;
	while (s1[j] != '\0' && s1[j] == s2[j])
		j++;
	if (j == s && j == ss)
		return (0);
	else if (s1[j] > s2[j])
		return (1);
	else if (s2[j] > s1[j])
		return (-1);
	return (300);
}
