int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	j;

	if (n == 0)
		return (0);
	j = 0;
	while (s1[j] != '\0' && s1[j] == s2[j] && j < n - 1)
		j++;
	return (s1[j] - s2[j]);
}
